def generate_pitch(business: dict, scenario: str, channel: str) -> str:
    """
    Генерирует персонализированный спитч для одного бизнеса.

    Args:
        business: Данные бизнеса (из парсера + обогащения)
        scenario: Ключ сценария ("microbusiness", "corporate_benefit", "partner_network")
        channel: Ключ канала ("whatsapp", "email", "cold_call")

    Returns:
        Готовый текст спитча
    """
    sc = SCENARIOS[scenario]
    ch = CHANNELS[channel]

    # Формируем контекст бизнеса
    biz_context = f"""Данные о компании:
- Название: {business.get('name', 'Неизвестно')}
- Сфера/тип: {business.get('types', 'Неизвестно')}
- Адрес: {business.get('address', '')}
- Рейтинг: {business.get('rating', 'нет данных')} ({business.get('reviews_count', 0)} отзывов)
- Сайт: {business.get('website', 'нет')}
- Телефон: {business.get('phone', 'нет')}
- Имя владельца/директора: {business.get('owner_name', 'неизвестно')}
- Email: {business.get('emails', 'нет')}
- Примеры отзывов клиентов: {business.get('reviews_sample', 'нет данных')}"""

    user_message = f"""{biz_context}

{ch['format_instruction']}

Напиши персонализированный спитч для этого бизнеса. Используй конкретные данные из карточки — название, сферу, отзывы. Текст должен выглядеть как написанный человеком, а не шаблон.

ВАЖНОЕ ПРАВИЛО: Не используй НИКАКУЮ markdown-разметку. Не используй звездочки (** или *), символы решетки (#), разделители (---), символы цитирования (>) или эмодзи в виде цифр (1️⃣). Используй только красивый чистый текст, как обычное текстовое сообщение."""

    # Вызов LLM
    headers = {
        "Authorization": f"Bearer {GROQ_API_KEY}",
        "Content-Type": "application/json",
    }

    payload = {
        "model": LLM_MODEL,
        "messages": [
            {"role": "system", "content": sc["system_prompt"]},
            {"role": "user", "content": user_message},
        ],
        "temperature": 0.8,
        "max_tokens": 1024,
    }

    for attempt in range(MAX_RETRIES):
        try:
            resp = requests.post(
                f"{LLM_BASE_URL}/chat/completions",
                headers=headers,
                json=payload,
                timeout=60,
            )

            # Rate limit — ждём и пробуем снова с экспоненциальной задержкой
            if resp.status_code == 429:
                # 15s, 30s, 60s, 120s...
                wait = (2 ** attempt) * 15 
                print(f"[rate limit! wait {wait}s...]", end=" ", flush=True)
                time.sleep(wait)
                continue

            resp.raise_for_status()
            data = resp.json()
            raw_text = data["choices"][0]["message"]["content"].strip()
            
            # Дополнительная очистка текста на уровне кода (на случай, если ИИ проигнорирует промпт)
            import re
            cleaned_text = re.sub(r'[*#_~`>]', '', raw_text) # Убираем markdown символы
            cleaned_text = re.sub(r'^\s*-\s*', '', cleaned_text, flags=re.MULTILINE) # Убираем тире-списки
            cleaned_text = re.sub(r'---', '', cleaned_text) # Убираем длинные тире
            
            return cleaned_text.strip()

        except requests.exceptions.HTTPError as e:
            if "429" in str(e) and attempt < MAX_RETRIES - 1:
                wait = (attempt + 1) * 10
                print(f"[rate limit, wait {wait}s]", end=" ")
                time.sleep(wait)
                continue
            return f"[ERROR: {e}]"
        except Exception as e:
            return f"[ERROR: {e}]"

    return "[ERROR: rate limit exceeded after retries]"


def generate_all_pitches(businesses: list[dict], scenario: str, channel: str) -> list[dict]:
    """
    Генерирует спитчи для всех бизнесов.
    Добавляет поле 'pitch' в каждый словарь.
    """
    sc_name = SCENARIOS[scenario]["name"]
    ch_name = CHANNELS[channel]["name"]
    print(f"\n[AI] Генерация спитчей: {sc_name} x {ch_name}")
    print(f"[AI] Модель: {LLM_MODEL}")
    print(f"[AI] Бизнесов: {len(businesses)}\n")

    for i, biz in enumerate(businesses, 1):
        print(f"  [{i}/{len(businesses)}] {biz['name']}...", end=" ")
        pitch = generate_pitch(biz, scenario, channel)
        biz[f"pitch_{scenario}_{channel}"] = pitch
        biz["last_scenario"] = sc_name
        biz["last_channel"] = ch_name
        print("OK")
        time.sleep(REQUEST_DELAY)  # Пауза между запросами

    return businesses
